package practice98;

public class Practice03 {
	public static void main(String[] args) {
		System.out.println("while문\n");
		int x = 6;
		while(x<5)
		{
			System.out.println("x의 값: "+x); 
			x++;
		}
		System.out.println("do-while문");
		do {
			System.out.println("x의 값"+x);
			x++;}
			while(x<5);
		
	}
}
